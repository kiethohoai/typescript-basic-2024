////////////// #21 TypeScript Switch Case ///////////////
const myAge: number = 30;

switch (myAge) {
    case 20:
        console.log("You're 20 years old!");
        break;
    case 25:
        console.log("You're 25 years old!");
        break;
    case 30:
        console.log("You're 30 years old!");
        break;
    default:
        console.log("How old are you?");
}
